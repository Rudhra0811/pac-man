function createGameBoard() {
    for (let y = 0; y < GRID_HEIGHT; y++) {
        for (let x = 0; x < GRID_WIDTH; x++) {
            const cell = document.createElement('div');
            cell.classList.add('cell');
            
            if (gameMap[y][x] === 1) {
                cell.classList.add('wall');
            } else if (gameMap[y][x] === 0) {
                cell.classList.add('dot');
            }
            
            gameBoard.appendChild(cell);
        }
    }
}

function createPacman() {
    const pacmanElement = document.createElement('div');
    pacmanElement.id = 'pacman';
    gameBoard.appendChild(pacmanElement);
    updatePacmanPosition();
}

function updatePacmanPosition() {
    const pacmanElement = document.getElementById('pacman');
    pacmanElement.style.left = `${pacman.x * 20}px`;
    pacmanElement.style.top = `${pacman.y * 20}px`;
}

function movePacman(dx, dy) {
    const newX = pacman.x + dx;
    const newY = pacman.y + dy;
    
    if (newX >= 0 && newX < GRID_WIDTH && newY >= 0 && newY < GRID_HEIGHT && gameMap[newY][newX] !== 1) {
        pacman.x = newX;
        pacman.y = newY;
        updatePacmanPosition();
    }
}

document.addEventListener('keydown', (event) => {
    switch (event.key) {
        case 'ArrowUp':
            movePacman(0, -1);
            break;
        case 'ArrowDown':
            movePacman(0, 1);
            break;
        case 'ArrowLeft':
            movePacman(-1, 0);
            break;
        case 'ArrowRight':
            movePacman(1, 0);
            break;
    }
});

